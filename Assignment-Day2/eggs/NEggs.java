package eggs;


public class NEggs 
{
	

	private int numEggs,gross,dozen,remainingEggs;
	
	public void setNumEggs(int numEggs) {
		this.numEggs = numEggs;
	}

	public void setGross(int gross) {
		this.gross = gross;
	}

	public void setDozen(int dozen) {
		this.dozen = dozen;
	}

	public void setRemainingEggs(int remainingEggs) {
		this.remainingEggs = remainingEggs;
	}

	public int getNumEggs() {
		return numEggs;
	}

	public int getGross() {
		return gross;
	}

	public int getDozen() {
		return dozen;
	}

	public int getRemainingEggs() {
		return remainingEggs;
	}
	

}
